homedir = os.environ['HOME']
nquakedir = homedir + '/.nquakesv'
pkgdir = '/usr/share/nquakesv'

def getVer():
    try:
        ver = 'unknown'
        pkginfo = subprocess.check_output(['pacman', '-Qi', 'nquakesv']).decode('utf-8').splitlines()
        for line in pkginfo:
            if line.startswith('Version'):
                ver = line.split(':')[1].strip()
                break
    except:
        ver = 'unknown'
    return(ver)

